package DataTypes;

public class DataType {

	public static void main(String[] args) {
		byte b=127;
		System.out.println("print value of byte:- "+ b);
		
		short s=32000;
		System.out.println("print value of short:- "+ s);
		
		int i=1000000000;
		System.out.println("print value of int:- "+ i);
		
		long l=1000000000000000000L;
		System.out.println("print value of long:- "+ l);
		
		double d=123.3456456;
		System.out.println("print value of double:- "+ d);
		
		float f=12.9990990909f;
		System.out.println("print value of float:- "+ f);
		
		char c='A';
		System.out.println("print value of char:- "+ c);
		
		boolean bool=true;
		System.out.println("print value of boolean:- "+ bool);
		
	}

} 
